package com.suresh.demos.conditionaloperators;

public class Demo6 {
	public static void main(String[] args) {
		String org = "ABC Technologies";
		int i;
		int total = 10;
		for (i = total; i > 0; i--) {
			System.out.println(org);
		}
		System.out.println("next....");
	}
}
